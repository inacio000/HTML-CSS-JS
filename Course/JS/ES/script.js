var n1 = 4
let n2 = 5
const n3 = 6

console.log(n1, n2, n3)

/*
    var - Variável global
        acessivel em qualquer parte do código

    const - Constante (sem reatribuição de valor)
        Variável local, acessível apenas dentro do "escopo/bloco" no qual ela foi criada.
        Quando lhe é atribuido algum valor, já não se pode atribuir mais nenhum.
        Mas é possível ser modificada.

    let - Variável local
        Diferente da 'var', o let é acessível apenas dentro do "escopo/bloco" no qual ela foi criada.
*/